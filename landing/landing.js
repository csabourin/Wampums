const html = document.documentElement;
const buttons = document.querySelectorAll(".lang-toggle button");

function applyTranslations(lang) {
	const copy = translations[lang];
	if (!copy) return;
	currentLang = lang; // Update global language tracker
	html.lang = lang;
	document.title =
		lang === "fr"
			? "Wampums - Gestion des scouts"
			: "Wampums - Scout Management";
	document.querySelectorAll("[data-i18n]").forEach((node) => {
		const key = node.getAttribute("data-i18n");
		if (copy[key]) {
			node.textContent = copy[key];
		}
	});
	buttons.forEach((btn) =>
		btn.setAttribute(
			"aria-pressed",
			btn.dataset.lang === lang ? "true" : "false",
		),
	);
	localStorage.setItem("wampums-lang", lang);
}

buttons.forEach((button) => {
	button.addEventListener("click", () =>
		applyTranslations(button.dataset.lang),
	);
});

// Global variable to track current language
let currentLang = "fr";

// Detect language based on URL path for static pages (/en/ or /fr/)
// Only apply JavaScript translations on the dynamic /index.html page
function initLanguage() {
	const path = window.location.pathname;
	
	// Check if we're on a static language-specific page
	if (path.startsWith('/en/') || path === '/en') {
		// English static page - update localStorage but don't apply JS translations
		// The HTML is already in English
		currentLang = "en";
		localStorage.setItem("wampums-lang", "en");
		html.lang = "en";
		return;
	}
	
	if (path.startsWith('/fr/') || path === '/fr') {
		// French static page - update localStorage but don't apply JS translations
		// The HTML is already in French
		currentLang = "fr";
		localStorage.setItem("wampums-lang", "fr");
		html.lang = "fr";
		return;
	}
	
	// For the dynamic page (/index.html or root), use localStorage preference
	const savedLang = localStorage.getItem("wampums-lang") || "fr";
	currentLang = savedLang;
	applyTranslations(savedLang);
}

initLanguage();

// Audience Tab Switching
function initTabs() {
	const tabBtns = document.querySelectorAll('.tab-btn');
	const tabPanels = document.querySelectorAll('.tab-panel');

	if (!tabBtns.length) return;

	tabBtns.forEach(btn => {
		btn.addEventListener('click', () => {
			const targetId = btn.getAttribute('aria-controls');

			// Deactivate all tabs
			tabBtns.forEach(b => {
				b.classList.remove('tab-btn--active');
				b.setAttribute('aria-selected', 'false');
			});

			tabPanels.forEach(p => {
				p.classList.remove('tab-panel--active');
				p.setAttribute('hidden', '');
			});

			// Activate selected tab
			btn.classList.add('tab-btn--active');
			btn.setAttribute('aria-selected', 'true');

			const panel = document.getElementById(targetId);
			if (panel) {
				panel.classList.add('tab-panel--active');
				panel.removeAttribute('hidden');
			}
		});

		// Keyboard arrow navigation
		btn.addEventListener('keydown', (e) => {
			const btns = Array.from(tabBtns);
			const idx = btns.indexOf(btn);
			let next = null;

			if (e.key === 'ArrowRight' || e.key === 'ArrowDown') {
				next = btns[(idx + 1) % btns.length];
			} else if (e.key === 'ArrowLeft' || e.key === 'ArrowUp') {
				next = btns[(idx - 1 + btns.length) % btns.length];
			}

			if (next) {
				e.preventDefault();
				next.focus();
				next.click();
			}
		});
	});
}

initTabs();

