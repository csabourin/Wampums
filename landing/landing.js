// Form validation
const validateEmail = (email) => {
	return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
};

const validateForm = () => {
	let isValid = true;
	const name = document.getElementById("name");
	const email = document.getElementById("email");
	const message = document.getElementById("message");

	// Clear previous errors
	document.querySelectorAll(".form-group.error").forEach((group) => {
		group.classList.remove("error");
	});

	// Validate name
	if (!name.value.trim() || name.value.trim().length < 2) {
		name.closest(".form-group").classList.add("error");
		isValid = false;
	}

	// Validate email
	if (!email.value.trim() || !validateEmail(email.value.trim())) {
		email.closest(".form-group").classList.add("error");
		isValid = false;
	}

	// Validate message
	if (!message.value.trim() || message.value.trim().length < 10) {
		message.closest(".form-group").classList.add("error");
		isValid = false;
	}

	return isValid;
};

// Handle form submission
demoForm.addEventListener("submit", async (e) => {
	e.preventDefault();

	if (!validateForm()) {
		return;
	}

	const submitButton = demoForm.querySelector(".btn-submit");
	submitButton.disabled = true;
	submitButton.textContent =
		currentLang === "fr" ? "Envoi en cours..." : "Sending...";

	try {
		const formData = {
			name: document.getElementById("name").value.trim(),
			email: document.getElementById("email").value.trim(),
			organization: document.getElementById("organization").value.trim(),
			phone: document.getElementById("phone").value.trim(),
			message: document.getElementById("message").value.trim(),
			honeypot: document.getElementById("honeypot").value,
		};

		const response = await fetch(getApiUrl(), {
			method: "POST",
			headers: {
				"Content-Type": "application/json",
			},
			body: JSON.stringify(formData),
		});

		const data = await response.json();

		if (response.ok && data.success) {
			// Show success message
			formSuccess.classList.add("active");
			demoForm.style.display = "none";

			// Redirect to demo experience after a brief confirmation pause
			setTimeout(() => {
				window.location.href = "https://demo.wampums.app";
			}, 2000);
		} else {
			// Show error
			alert(
				data.message ||
				(currentLang === "fr"
					? "Une erreur est survenue. Veuillez reessayer."
					: "An error occurred. Please try again."),
			);
			submitButton.disabled = false;
			submitButton.textContent =
				currentLang === "fr" ? "Envoyer la demande" : "Submit request";
		}
	} catch (error) {
		console.error("Error submitting form:", error);
		alert(
			currentLang === "fr"
				? "Une erreur est survenue. Veuillez reessayer."
				: "An error occurred. Please try again.",
		);
		submitButton.disabled = false;
		submitButton.textContent =
			currentLang === "fr" ? "Envoyer la demande" : "Submit request";
	}
});
